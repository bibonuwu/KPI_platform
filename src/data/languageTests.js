// Normalize raw question arrays to the shape expected by the quiz UI:
//   { id, text, options: [{ key, text }], correct: <key> }
// Options are randomized per test render via shuffle() at the call site if needed.
function normalize(raw, lang, level) {
  return raw.map((q, idx) => {
    const optionKeys = ["A", "B", "C", "D"];
    const options = (q.options || []).map((text, i) => ({ key: optionKeys[i] || String(i + 1), text }));
    const correctOpt = options.find(o => o.text === q.correct);
    return {
      id: `${lang}_${level}_q${idx + 1}`,
      text: q.text,
      options,
      correct: correctOpt ? correctOpt.key : "A"
    };
  });
}

export const LANGUAGE_TESTS = (() => {
  const out = {};
  for (const lang of Object.keys(LANG_TESTS_RAW)) {
    out[lang] = {};
    for (const lvl of CEFR_LEVELS) {
      out[lang][lvl] = normalize(LANG_TESTS_RAW[lang][lvl] || [], lang, lvl);
    }
  }
  return out;
})();

export function getLanguageTest(lang, level) {
  return LANGUAGE_TESTS?.[lang]?.[level] || [];
}

export function cefrPassThreshold() { return 70; }

export function compareCefr(a, b) {
  const oa = CEFR_LEVEL_META[a]?.order || 0;
  const ob = CEFR_LEVEL_META[b]?.order || 0;
  return oa - ob;
}

export function highestCefr(...levels) {
  let best = null;
  for (const l of levels) {
    if (!l) continue;
    if (!best || compareCefr(l, best) > 0) best = l;
  }
  return best;
}

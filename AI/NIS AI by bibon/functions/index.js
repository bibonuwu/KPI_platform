/** Применяет ALIASES к нормализованному тексту, расширяя его. */
function expandAliases(t) {
  if (!t) return t;
  const tokens = t.split(" ").filter(Boolean);
  const extras = [];
  for (const tok of tokens) {
    if (ALIASES[tok]) extras.push(ALIASES[tok]);
  }
  return extras.length ? `${t} ${extras.join(" ")}` : t;
}

/**
 * Транслитерация латиницы в кириллицу для коротких токенов.
 * Применяется к КАЖДОМУ латинскому токену длиной 3–14, сохраняем оригинал.
 */
function translitWord(w) {
  if (!/^[a-z]+$/.test(w) || w.length < 3 || w.length > 14) return null;
  let out = w;
  for (const [from, to] of TRANSLIT_RULES) {
    out = out.split(from).join(to);
  }
  // Только если в итоге всё кириллицей — иначе игнор.
  if (/^[а-яё]+$/.test(out) && out !== w) return out;
  return null;
}

function normalize(s) {
  let base = String(s || "")
    .toLowerCase()
    .replace(/[ё]/g, "е")
    .replace(/[^\p{L}\p{N}\s]+/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
  if (!base) return base;
  // Раскрываем алиасы + дописываем транслит-варианты латинских токенов.
  const tokens = base.split(" ");
  const translit = [];
  for (const tok of tokens) {
    const r = translitWord(tok);
    if (r) translit.push(r);
  }
  if (translit.length) base = `${base} ${translit.join(" ")}`;
  return expandAliases(base);
}

/** Левенштейн ≤ max — для коротких слов, чтобы прощать опечатки. */
function levenshteinLE(a, b, max) {
  const la = a.length, lb = b.length;
  if (Math.abs(la - lb) > max) return max + 1;
  // Полный O(la*lb) — но на коротких токенах (≤16) это копейки.
  const dp = new Array(lb + 1);
  for (let j = 0; j <= lb; j++) dp[j] = j;
  for (let i = 1; i <= la; i++) {
    let prev = dp[0]; dp[0] = i; let rowMin = dp[0];
    for (let j = 1; j <= lb; j++) {
      const tmp = dp[j];
      dp[j] = a[i - 1] === b[j - 1] ? prev : 1 + Math.min(prev, dp[j], dp[j - 1]);
      prev = tmp;
      if (dp[j] < rowMin) rowMin = dp[j];
    }
    if (rowMin > max) return max + 1; // ранний выход
  }
  return dp[lb];
}

/** Нечёткое вхождение: слово из текста почти совпадает с ключом. */
function fuzzyMatch(textTokens, key) {
  // ключ длиной ≥5 → допускаем 1 опечатку; иначе — только точное вхождение
  if (key.length < 5) return false;
  for (const tok of textTokens) {
    if (tok.length < 4) continue;
    if (Math.abs(tok.length - key.length) > 1) continue;
    if (levenshteinLE(tok, key, 1) <= 1) return true;
  }
  return false;
}

/**
 * Совпадает ли ключ kw с текстом.
 *  - Однословный ключ: подстрока ИЛИ нечёткое совпадение (Левенштейн ≤1).
 *  - Многословный ключ ("добав достижен"): все слова должны быть подстроками
 *    нормализованного текста — не обязательно подряд. Это критично, чтобы
 *    «как добавить достижение?» матчилось ключу «добав достижен».
 */
function keyMatches(text, tokens, nk) {
  if (!nk) return false;
  if (!nk.includes(" ")) {
    if (text.includes(nk)) return true;
    return fuzzyMatch(tokens, nk);
  }
  const parts = nk.split(" ").filter(Boolean);
  if (parts.length < 2) return text.includes(nk);
  for (const p of parts) {
    if (p.length >= 4) {
      if (text.includes(p)) continue;
      if (fuzzyMatch(tokens, p)) continue;
      return false;
    } else {
      // короткие части (1-3 символа) ищем строго как подстроку
      if (!text.includes(p)) return false;
    }
  }
  return true;
}

function score(text, kw) {
  // Считаем количество совпавших ключевых слов и взвешиваем по длине/количеству частей.
  const tokens = text.split(" ").filter(Boolean);
  let s = 0;
  let hits = 0;
  for (const k of kw) {
    const nk = normalize(k);
    if (!nk) continue;
    if (!keyMatches(text, tokens, nk)) continue;
    hits++;
    // Многословные ключи — сильнее (явный сигнал намерения).
    const parts = nk.split(" ").filter(p => p.length >= 3);
    if (parts.length >= 2) {
      s += 4 + parts.length; // 6 за 2 части, 7 за 3 и т.д.
    } else {
      s += nk.length >= 6 ? 4 : nk.length >= 4 ? 3 : nk.length >= 3 ? 2 : 1;
    }
  }
  if (hits >= 2) s += 2;
  if (hits >= 4) s += 2;
  return s;
}

/**
 * Извлекает строку из элемента answers — поддерживает оба формата:
 *  - "обычная строка"
 *  - { ru: "...", kz: "...", en: "..." }
 * Падающая цепочка: lang → ru → en → kz → первое непустое.
 */
function pickLang(item, lang) {
  if (typeof item === "string") return item;
  if (item && typeof item === "object") {
    return item[lang]
      || item.ru
      || item.en
      || item.kz
      || Object.values(item).find(v => typeof v === "string" && v.trim())
      || "";
  }
  return String(item || "");
}

function pickAnswer(intent, lang) {
  const arr = intent.answers || [];
  if (!arr.length) return "";
  const item = arr[Math.floor(Math.random() * arr.length)];
  return pickLang(item, lang || "ru");
}

/** Возвращает массив строк-followup для клиента: лейблы из FOLLOWUP_LABELS. */
function followupsFor(intentId, lang) {
  const meta = getMeta(intentId);
  return (meta.followups || []).map(id => followupLabel(id, lang)).filter(Boolean).slice(0, 4);
}

/** Основная функция: возвращает {reply, suggestions, topic, intent}. */
function answerFor(userText, lang) {
  const t = normalize(userText);
  const L = lang || "ru";

  if (!t) {
    return {
      reply: L === "kz"
        ? "Сұрақ жазыңыз — мысалы, «жетістікті қалай қосуға болады?» немесе «PhD-ге қанша ұпай?»."
        : L === "en"
          ? "Ask me anything about the platform — e.g. \"How do I submit an achievement?\" or \"Points for PhD?\"."
          : "Напишите вопрос — например, «как добавить достижение?» или «сколько баллов за PhD?».",
      suggestions: ["capabilities", "add-achievement", "rating", "request"].map(id => followupLabel(id, L)),
      topic: "default",
      intent: null,
    };
  }

  // Считаем score по всем интентам.
  const scored = [];
  for (const intent of INTENTS) {
    const s = score(t, intent.kw);
    if (s > 0) scored.push({ intent, s });
  }
  scored.sort((a, b) => b.s - a.s);

  // Подстраховка: «add-achievement» ловим по парам глагол+объект, даже если
  // ни один многословный ключ не сработал. Это покрывает короткие формы вида
  // «как добавить kpi», «оформить достижение», «жаңа жетістік қос», «add kpi».
  const ACTION_RE = /(добав|оформ|созда|подат|залит|загруз|внес|регистр|зарегистр|how to add|how do i add|how to submit|how do i submit|add a|add new|submit|create|log a|register a|енгіз|тапсыр|ұсын|қалай қос|жаңа жетіс|add achievement|file)/;
  const OBJECT_RE = /(kpi|достижен|балл|очк|запис|kpi|жетіс|ұпай|achievement|points|score|record)/;
  const isAddIntent = best => best && best.intent && best.intent.id === "add-achievement";
  if (ACTION_RE.test(t) && OBJECT_RE.test(t) && !isAddIntent(scored[0])) {
    const addEntry = INTENTS.find(i => i.id === "add-achievement");
    if (addEntry) {
      // вставляем add-achievement с весом чуть выше текущего лидера,
      // но без перебивания явных совпадений на специфичные интенты
      const top = scored[0] ? scored[0].s : 0;
      scored.unshift({ intent: addEntry, s: Math.max(top + 1, 5) });
    }
  }

  const best = scored[0] || null;
  const second = scored[1] || null;

  // Уточняющий вопрос, если top-2 близки и оба «слабые-средние» (3–5).
  if (best && second && best.s >= 3 && best.s - second.s <= 1 && best.s <= 5) {
    const a = followupLabel(best.intent.id, L);
    const b = followupLabel(second.intent.id, L);
    const head = L === "kz"
      ? "Сұрағыңыз нені білдіреді?"
      : L === "en" ? "Did you mean one of these?" : "Уточните — вы про что?";
    return {
      reply: `${head}\n- ${a}\n- ${b}`,
      suggestions: [a, b],
      topic: getMeta(best.intent.id).topic,
      intent: null,
    };
  }

  if (!best || best.s < 2) {
    const reply = L === "kz"
      ? "Сұрақты түсінбедім. Мен білемін: жетістіктер мен ұпайлар (олимпиада, IELTS, мақалалар), рейтинг пен деңгейлер, тоқсандар, өтінімдер (еркін күн/демалыстағы жұмыс), құжаттар, профиль, тема мен тіл, әкімші панелі, директор, СКУД, оқиғалар, қолдау. Нақтырақ сұраңыз."
      : L === "en"
        ? "I didn't get the question. I know about: achievements & points (olympiads, IELTS, papers), rating & levels, quarters, requests (leave / weekend work), documents, profile, theme & language, admin panel, director, SKUD, events, support. Please be more specific."
        : "Я не понял вопрос. Я знаю про: добавление достижений и баллы (олимпиады, IELTS, статьи), рейтинг и уровни, четверти, заявки (отгул/работа в выходной), документы, профиль, темы и языки, админ-кабинет, директора, СКУД, события, поддержку. Спросите конкретно — например: «сколько баллов за PhD?», «как взять отгул?», «что такое уровень мастер?»";
    return {
      reply,
      suggestions: ["capabilities", "add-achievement", "rating", "request"].map(id => followupLabel(id, L)),
      topic: "default",
      intent: null,
    };
  }

  return {
    reply: pickAnswer(best.intent, L),
    suggestions: followupsFor(best.intent.id, L),
    topic: getMeta(best.intent.id).topic,
    intent: best.intent.id,
  };
}

/* --------------------------- HTTP-хендлер --------------------------- */

function corsHeaders() {
  return {
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Methods": "POST, OPTIONS",
    "Access-Control-Allow-Headers": "Content-Type",
    "Access-Control-Max-Age": "3600",
  };
}

function sendJson(res, status, body) {
  res.set(corsHeaders());
  res.status(status).json(body);
}

exports.chat = onRequest(
  { region: "us-central1", cors: false, memory: "128MiB", timeoutSeconds: 10, invoker: "public" },
  async (req, res) => {
    if (req.method === "OPTIONS") {
      res.set(corsHeaders());
      res.status(204).send("");
      return;
    }
    if (req.method !== "POST") {
      sendJson(res, 405, { error: "method_not_allowed" });
      return;
    }

    let body = req.body;
    if (typeof body === "string") {
      try { body = JSON.parse(body); } catch { body = {}; }
    }
    const messages = Array.isArray(body?.messages) ? body.messages : [];
    const last = [...messages].reverse().find(m => m && m.role === "user" && typeof m.content === "string");
    if (!last) {
      sendJson(res, 400, { error: "messages_required" });
      return;
    }

    const lang = (body?.lang === "kz" || body?.lang === "en") ? body.lang : "ru";
    const result = answerFor(String(last.content).slice(0, 1000), lang);
    sendJson(res, 200, {
      reply: result.reply,
      suggestions: result.suggestions || [],
      topic: result.topic || "default",
      intent: result.intent || null,
      version: AI_VERSION,
      versionDate: AI_VERSION_DATE,
      versionName: AI_VERSION_NAME,
    });
  }
);
